package com.example.springtrial.simple;

public class FactoryGeneratedObject {
	private double id;

	/**
	 * We set constructor to default, so its bean can not be created from class
	 * external to package.
	 */
	FactoryGeneratedObject() {
	}

	public void setId(double id) {
		this.id = id;
	}

	public double getId() {
		return id;
	}

}
